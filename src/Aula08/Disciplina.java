package Aula08;

public class Disciplina {
	private double nota;
	private String nome;
	
	public Disciplina() {
		
	}

	public double getNota() {
		return nota;
	}

	public void setNota(double nota) {
		this.nota = nota;
	}

	public String getNome() {
		return nome;
	}

	public void setNome(String nome) {
		this.nome = nome;
	}
	
}
